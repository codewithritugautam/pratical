package controlstatement;

public class Switch3 {
public static void main(String[] args) {
	char ch='r';
    switch(ch)	{
    case 'z':
    	System.out.println("sana");
    	break;
    case 'p':
    	System.out.println("ritu");
    break;
    case 'r':
    	System.out.println("ritu gautam");
    	break;
    	default: System.out.println("Invalid");
    }	
}
}
