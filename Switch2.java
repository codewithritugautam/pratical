public class Switch2 {
	public static void main(String[] args) {
		char ch='a';
		switch(ch) {
        case 'd':
			System.out.println("first case execute");
			break;
        case 'a':
    		System.out.println("first case execute");
    		break;
        case 'y':
    		System.out.println("first case execute");
    		break;
        case 'x':
    		System.out.println("first case execute");
    		break;
    		default: System.out.println("Invalid");
		}
	}

}
